-- 1. Retrieve the names of all products and their respective categories
SELECT product_name, category_name
FROM Products
JOIN Categories ON Products.category_id = Categories.category_id;

-- 2. Identify the customer who has placed the order with the highest total value
SELECT Customers.customer_id, Customers.customer_name, MAX(order_total) AS highest_order_value
FROM Orders
JOIN Customers ON Orders.customer_id = Customers.customer_id
GROUP BY Customers.customer_id, Customers.customer_name
ORDER BY highest_order_value DESC
LIMIT 1;

